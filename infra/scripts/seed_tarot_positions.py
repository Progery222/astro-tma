# ── Main seed function ────────────────────────────────────────────────────────
async def seed():
    from services.tarot.seed_data import get_all_cards_seed, MAJOR_ARCANA
    from services.tarot.engine import SPREADS

    cards_data = get_all_cards_seed()

    async with AsyncSessionLocal() as session:
        # Clear existing position meanings
        await session.execute(delete(TarotPositionMeaning))

        # Load all cards from DB to get their IDs
        result = await session.execute(select(TarotCard).order_by(TarotCard.id))
        db_cards = list(result.scalars())

        if not db_cards:
            print("No cards in DB! Run seed_tarot.py first.")
            return

        # Build meanings for each card
        count = 0
        for db_card in db_cards:
            # Find card data
            card_data = None
            for cd in cards_data:
                if cd["name_en"] == db_card.name_en:
                    card_data = cd
                    break

            if not card_data:
                continue

            # Get meanings dict
            is_major = db_card.arcana.value == "major"
            if is_major and db_card.number in MAJOR_MEANINGS:
                meanings = MAJOR_MEANINGS[db_card.number]
            else:
                meanings = _minor_meanings(
                    card_data.get("element", "fire"),
                    db_card.number,
                    db_card.name_ru,
                )

            # Generate position meanings for all spreads
            for spread_type, positions in SPREADS.items():
                templates = POSITION_CONTEXTS[spread_type]
                for pos in positions:
                    template = templates[pos.index]
                    try:
                        meaning_text = template.format(**meanings)
                    except KeyError:
                        meaning_text = f"{pos.name_ru}: {meanings.get('theme', db_card.name_ru)}"

                    session.add(TarotPositionMeaning(
                        card_id=db_card.id,
                        spread_type=spread_type,
                        position=pos.index,
                        position_name_ru=pos.name_ru,
                        meaning_ru=meaning_text,
                    ))
                    count += 1

        await session.commit()
        print(f"Seeded {count} tarot position meanings")


asyncio.run(seed())
