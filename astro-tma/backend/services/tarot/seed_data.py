def get_all_cards_seed() -> list[dict]:
    """Return all 78 cards ready for DB insertion."""
    cards = list(MAJOR_ARCANA)  # 22 major

    # Generate 56 minor arcana (4 suits × 14 cards)
    suits = [
        ("wands",     "Жезлы",     "fire",  "🔥"),
        ("cups",      "Кубки",     "water", "🌊"),
        ("swords",    "Мечи",      "air",   "⚔️"),
        ("pentacles", "Пентакли",  "earth", "🪙"),
    ]
    pip_names = {
        1: "Туз", 2: "Двойка", 3: "Тройка", 4: "Четвёрка",
        5: "Пятёрка", 6: "Шестёрка", 7: "Семёрка", 8: "Восьмёрка",
        9: "Девятка", 10: "Десятка",
    }
    court_names = {11: ("Паж", "Page"), 12: ("Рыцарь", "Knight"),
                   13: ("Королева", "Queen"), 14: ("Король", "King")}

    for suit_en, suit_ru, element, emoji in suits:
        for num in range(1, 15):
            if num <= 10:
                name_ru = f"{pip_names[num]} {suit_ru}"
                name_en = f"{num if num > 1 else 'Ace'} of {suit_en.title()}"
            else:
                court_ru, court_en = court_names[num]
                name_ru = f"{court_ru} {suit_ru}"
                name_en = f"{court_en} of {suit_en.title()}"

            cards.append({
                "number": num,
                "name_en": name_en,
                "name_ru": name_ru,
                "emoji": emoji,
                "element": element,
                "upright_ru": f"Значение {name_ru} в прямом положении — позитивные аспекты {suit_ru.lower()} стихии.",
                "reversed_ru": f"Значение {name_ru} в перевёрнутом положении — заблокированная энергия {suit_ru.lower()}.",
                "keywords_ru": [suit_en, f"number_{num}"],
            })

    return cards
